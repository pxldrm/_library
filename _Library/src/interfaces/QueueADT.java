package interfaces;
public interface QueueADT<T>{
//************************************************************
/**
*	Access
*/
//************************************************************
	public int 		size();
	public T 		first();
//************************************************************
/**
*	Boolean
*/
//************************************************************    
	public boolean 	isEmpty();
//************************************************************
/**
*	Modify
*/
//************************************************************
	public void 	enqueue(T element);
	public T 		dequeue();
//************************************************************
/**
*	toString
*/
//************************************************************
	public String 	toString();
}
