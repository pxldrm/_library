package interfaces;
public interface StackADT<T>{
//************************************************************
/**
*	Access
*/
//************************************************************
	public int 		size();
	public T 		peek();
//************************************************************
/**
*	Booleans
*/
//************************************************************    
	public boolean 	isEmpty();
//************************************************************
/**
*	Mutators
*/
//************************************************************
	public void 	push(T element);
	public T 		pop();
//************************************************************
/**
*	toString
*/
//************************************************************
	public String 	toString();
}
